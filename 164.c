#include<stdio.h>
int main() {
    int n, m, a;
    scanf("%d %d", &n, &m);
    a = n / m;
    if (n % m == 0) {
        printf ("%d\n", a);
    } else {
        printf ("%d\n", a + 1);
    }
    return 0;
}
