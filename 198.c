#include <stdio.h>

long long a[1000000];
int main() {
    int n,m;
    scanf("%d", &n);
    a[1]=1;
    a[2]=1;
    for(int i = 3;  i <= n; i++) {
        a[i] = (a[i-1]+a[i-2])%1000000007;
    }
    m = a[n] % 1000000007;
    printf("%d\n", m);
    return 0;
}
