#include <stdio.h>
#include <math.h>
int main() {
    double r, h, s;
    scanf ("%lf %lf", &r, &h);
    s = r * ((4 + 3.14) * r + (3.14 + 2 + 2 * sqrt(2)) * h);
    printf ("%.2lf\n", s);
}
