#include<stdio.h>
int main() {
    double n, m = 0;
    int j;
    scanf ("%lf",&n);
    for (j = 1; j <= 6; j++) {     
        m = (1 + 0.00417) * (n + m);
    }
    printf ("$%.2lf", m);
    return 0;
}
